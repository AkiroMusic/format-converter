/**
 * KGM / KGMA / VPR decoder (KuGou Music v2 cache) — pure TS, no native deps.
 *
 * Ported from OpenConverter reference implementation (Apache 2.0).
 *
 * File layout:
 *   0x00..0x10  16-byte magic (KGM or VPR header)
 *   0x10..0x14  4-byte uint32 LE header length
 *   0x14..0x1C  4 bytes reserved
 *   0x1C..0x2C  16-byte per-file key (key[16] is implicitly 0)
 *   0x2C..headerLen  Reserved
 *   headerLen..end   Encrypted audio
 *
 * Decryption per byte at position i:
 *   med8 = key[(i + offset) % 17] ^ blockData[i]
 *   med8 ^= (med8 & 0x0F) << 4
 *   msk8 = getMask(i + offset)
 *   msk8 ^= (msk8 & 0x0F) << 4
 *   blockData[i] = med8 ^ msk8
 *   if isVpr: blockData[i] ^= VprMaskDiff[(i + offset) % 17]
 */

