/**
 * Compute the mask for a given position.
 */
function getMask(pos: number): number {
  let offset = pos >> 4
  let value = 0
  while (offset >= 0x11) {
    value ^= TABLE1[offset % 272]
    offset >>= 4
    value ^= TABLE2[offset % 272]
    offset >>= 4
  }
  return MASK_V2_PRE_DEF[pos % 272] ^ value
}

/**
 * Decrypt a KGM/KGMA/VPR buffer in-place. Returns the decrypted audio bytes.
 */
function decryptBuffer(kgmBuf: Uint8Array): Uint8Array {
  if (kgmBuf.length < 0x2C) {
    throw new Error(`KGM/VPR: file too small (${kgmBuf.length} < 0x2C)`)
  }

  // Check magic
  let isVpr: boolean
  let magicMatch = true
  for (let i = 0; i < 16; i++) {
    if (kgmBuf[i] !== KGM_HEADER[i]) { magicMatch = false; break }
  }
  if (magicMatch) {
    isVpr = false
  } else {
    magicMatch = true
    for (let i = 0; i < 16; i++) {
      if (kgmBuf[i] !== VPR_HEADER[i]) { magicMatch = false; break }
    }
    if (magicMatch) {
      isVpr = true
    } else {
      throw new Error('KGM/VPR: bad magic, expected KGM or VPR header')
    }
  }

  const headerLen = readUint32LE(kgmBuf, 0x10)
  if (headerLen < 0x2C || headerLen > kgmBuf.length) {
    throw new Error(`KGM/VPR: invalid header length ${headerLen}`)
  }

  // 17-byte key: 16 bytes at offset 0x1C, then key[16] = 0
  const key = new Uint8Array(17)
  for (let i = 0; i < 16; i++) {
    key[i] = kgmBuf[0x1C + i]
  }
  key[16] = 0

  const audio = kgmBuf.slice(headerLen)
  for (let i = 0; i < audio.length; i++) {
    let med8 = key[i % 17] ^ audio[i]
    med8 ^= (med8 & 0x0F) << 4
    let msk8 = getMask(i)
    msk8 ^= (msk8 & 0x0F) << 4
    audio[i] = med8 ^ msk8
    if (isVpr) {
      audio[i] ^= VPR_MASK_DIFF[i % 17]
    }
  }

  return audio
}

/**
 * Decode (parse + decrypt) a KGM/KGMA/VPR file from ArrayBuffer.
 */
async function parseKGM(
  arrayBuffer: ArrayBuffer,
  options?: { onProgress?: (progress: number) => void; onWarning?: (message: string) => void }
): Promise<DecoderResult> {
  const data = new Uint8Array(arrayBuffer)

  options?.onProgress?.(0.1)

  const audioData = decryptBuffer(data)

  options?.onProgress?.(0.8)

  const format = detectAudioFormat(audioData)

  options?.onProgress?.(1.0)

  return {
    audioData,
    format,
    metadata: null,
    image: null,
    imageMime: null,
    songName: 'Unknown',
    artist: 'Unknown',
    album: 'Unknown'
  }
}

